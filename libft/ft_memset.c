#include <string.h>

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	char			*ss;

	ss = s;
	i = 0;
	while (i < n)
	{
		ss[i] = c;
		i++;
	}
	return (s);
}
