char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*r;

	i = 0;
	r = 0;
	while (s[i])
	{
		if (s[i] == (unsigned char)c)
			r = (char *)&s[i];
		i++;
	}
	if ((unsigned char)c == '\0')
		r = (char *)&s[i];
	return (r);
}
